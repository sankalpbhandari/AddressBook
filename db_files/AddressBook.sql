KEY `fk_ADDRESS_1_idx` (`ContactID`),
CONSTRAINT `fk_ADDRESS_1` FOREIGN KEY (`ContactID`) REFERENCES `CONTACT` (`ContactID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=latin1;
