KEY `ContactID_idx` (`ContactID`),
KEY `fk_DATE_1_idx` (`ContactID`),
CONSTRAINT `fk_DATE_1` FOREIGN KEY (`ContactID`) REFERENCES `CONTACT` (`ContactID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB AUTO_INCREMENT=4 DEFAULT CHARSET=latin1;
CREATE TABLE `PHONE` (
