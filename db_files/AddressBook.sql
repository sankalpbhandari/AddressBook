USE AddressBook;
CREATE TABLE `CONTACT` (
`ContactID` int(11) NOT NULL AUTO_INCREMENT,
`Fname` varchar(45) NOT NULL,
`Mname` varchar(45) DEFAULT NULL,
`Lname` varchar(45) NOT NULL,
